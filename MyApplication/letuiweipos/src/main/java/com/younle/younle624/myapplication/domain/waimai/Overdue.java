public class Overdue {
    private String alert;
    private int type;

    public Overdue(String alert, int type) {
        this.alert = alert;
        this.type = type;
    }

    public String getAlert() {
        return alert;
    }

    public void setAlert(String alert) {
        this.alert = alert;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return "Overdue{" +
                "alert='" + alert + '\'' +
                ", type=" + type +
                '}';
    }
}
