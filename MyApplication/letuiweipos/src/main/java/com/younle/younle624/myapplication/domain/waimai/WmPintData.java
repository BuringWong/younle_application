public class WmPintData {
    public static final int CENTER = 1;
    public static final int LEFT = 0;
    public static final int RIGHT = 2;
    public static final int QRCODE = 1;
    private int textSize = 27;
    private String msg;
    private int alin = 0;
    private int type = 0;
    public WmPintData() {
    }

    public WmPintData(String msg) {
        this.msg = msg;
    }

    public WmPintData(int textSize, String msg) {
        this.textSize = textSize;
        this.msg = msg;
    }


    public WmPintData( String msg,int textSize, int alin) {
        this.textSize = textSize;
        this.msg = msg;
        this.alin = alin;
    }

    public WmPintData(int textSize, String msg, int alin, int type) {
        this.textSize = textSize;
        this.msg = msg;
        this.alin = alin;
        this.type = type;
    }

    public int getAlin() {
        return alin;
    }

    public void setAlin(int alin) {
        this.alin = alin;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getTextSize() {
        return textSize;
    }

    public void setTextSize(int textSize) {
        this.textSize = textSize;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    @Override
    public String toString() {
        return "WmPintData{" +
                "textSize=" + textSize +
                ", msg='" + msg + '\'' +
                '}';
    }
}
