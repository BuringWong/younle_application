public class PrintFormatBean {
    private String content;//要打印的内容
    private  int alignment;//@param alignment:	对齐方式 0--居左 , 1--居中, 2--居右
    private int linewrap;//换几行

    public PrintFormatBean() {
    }

    public PrintFormatBean(String content, int alignment, int linewrap) {
        this.content = content;
        this.alignment = alignment;
        this.linewrap = linewrap;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public int getAlignment() {
        return alignment;
    }

    public void setAlignment(int alignment) {
        this.alignment = alignment;
    }

    public int getLinewrap() {
        return linewrap;
    }

    public void setLinewrap(int linewrap) {
        this.linewrap = linewrap;
    }

    @Override
    public String toString() {
        return "PrintFormatBean{" +
                "content='" + content + '\'' +
                ", alignment=" + alignment +
                ", linewrap=" + linewrap +
                '}';
    }
}
